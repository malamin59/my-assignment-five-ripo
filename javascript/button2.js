document.getElementById("completed-btn").addEventListener('click', function() {

    let navBarValue = document.getElementById('navbar-value');
    let contentValue = document.getElementById('container-value');

    navBarValue.textContent= parseInt(navBarValue.textContent) + 1;
    contentValue.textContent =parseInt(contentValue.textContent) - 1;
})
